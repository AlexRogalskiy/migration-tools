package com.nuodb.tools.migration.cli.handler.option;

import com.nuodb.tools.migration.cli.handler.Trigger;

/**
 * @author Sergey Bushik
 */
public class SimpleTrigger implements Trigger {

    private String trigger;

    public SimpleTrigger(String trigger) {
        this.trigger = trigger;
    }

    @Override
    public boolean fire(String value) {
        return trigger.equals(value);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        SimpleTrigger that = (SimpleTrigger) o;

        if (trigger != null ? !trigger.equals(that.trigger) : that.trigger != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return trigger != null ? trigger.hashCode() : 0;
    }

    @Override
    public String toString() {
        return trigger;
    }
}
